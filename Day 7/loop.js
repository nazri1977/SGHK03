//for loop
for (let i = 0; i < 10; i++) {
    console.log("Hello OneCoder! ", i);
  }
  
  //while loop
  let count = 1;
  
  while (count <= 5) {
    console.log("Count: ", count);
    count++;
  }
  
  //do-while loop
  let i = 7;
  do {
    console.log("Do-while counter: ", i);
    i++;
  } while (i <= 5);
