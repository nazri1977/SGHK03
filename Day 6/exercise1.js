// Define a constant variable 'kelvin' and set its value to 293
const kelvin = 293;

// Convert Kelvin to Celsius by subtracting 273 from the 'kelvin' variable
const celsius = kelvin - 273;

// Convert Celsius to Fahrenheit using the formula: Fahrenheit = Celsius * (9/5) + 32
const fahrenheit = celsius * (9/5) + 32;

// Log the Fahrenheit temperature to the console
console.log(`The temperature is ${fahrenheit} degrees Fahrenheit.`);
